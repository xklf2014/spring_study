package com.story.proxy.service.impl;


public class MyCalculator1 {
    public Integer add(int i, int j) {
        return i + j;
    }

    public Integer mins(int i, int j) {
        return i - j;
    }

    public Integer mul(int i, int j) {
        return i * j;
    }

    public Integer sub(int i, int j) {
        return i / j;
    }
}
